export const addNewDoctor = catchAsyncErrors(async (req, res, next) => {
  if (!req.files || Object.keys(req.files).length === 0) {
    return next(new ErrorHandler("Doctor Avatar Required!", 400));
  }
  const { docAvatar } = req.files;
  const allowedFormats = ["image/png", "image/jpeg", "image/webp"];
  if (!allowedFormats.includes(docAvatar.mimetype)) {
    return next(new ErrorHandler("File Format Not Supported!", 400));
  }

  const {
    firstName,
    lastName,
    email,
    phone,
    nic,
    dob,
    gender,
    password,
    doctorDepartment,
    availability, // This will be an array of objects containing day and working hours
  } = req.body;

  if (
    !firstName ||
    !lastName ||
    !email ||
    !phone ||
    !nic ||
    !dob ||
    !gender ||
    !password ||
    !doctorDepartment ||
    !availability || // Check availability is provided
    !docAvatar
  ) {
    return next(new ErrorHandler("Please Fill Full Form!", 400));
  }

  // Check if doctor is already registered
  const isRegistered = await User.findOne({ email });
  if (isRegistered) {
    return next(
      new ErrorHandler("Doctor With This Email Already Exists!", 400)
    );
  }

  // Upload Avatar to Cloudinary
  const cloudinaryResponse = await cloudinary.uploader.upload(
    docAvatar.tempFilePath
  );
  if (!cloudinaryResponse || cloudinaryResponse.error) {
    console.error(
      "Cloudinary Error:",
      cloudinaryResponse.error || "Unknown Cloudinary error"
    );
    return next(
      new ErrorHandler("Failed To Upload Doctor Avatar To Cloudinary", 500)
    );
  }

  // Create the doctor with availability
  const doctor = await User.create({
    firstName,
    lastName,
    email,
    phone,
    nic,
    dob,
    gender,
    password,
    role: "Doctor",
    doctorDepartment,
    availability: JSON.parse(availability), // Parse the availability from JSON string
    docAvatar: {
      public_id: cloudinaryResponse.public_id,
      url: cloudinaryResponse.secure_url,
    },
  });

  res.status(200).json({
    success: true,
    message: "New Doctor Registered",
    doctor,
  });
});

export const getAvailableTimes = catchAsyncErrors(async (req, res, next) => {
  const { doctorId, selectedDate } = req.query; // Assume 'selectedDate' is passed in 'YYYY-MM-DD' format

  // Find the doctor by ID
  const doctor = await User.findById(doctorId);
  if (!doctor) {
    return next(new ErrorHandler("Doctor Not Found!", 404));
  }

  // Get the availability object for the doctor
  const { availability } = doctor;

  // Parse the selected date and get the corresponding day of the week
  const selectedDayOfWeek = moment(selectedDate, "YYYY-MM-DD").format("dddd");

  // Check if the doctor has availability on the selected day
  const dayAvailability = availability[selectedDayOfWeek];

  if (!dayAvailability || !dayAvailability.available) {
    return res.status(200).json({
      success: true,
      availableTimes: {}, // No available times for the selected date
    });
  }

  // Return the available times for the selected date
  const availableTimesForSelectedDate = {
    [selectedDate]: {
      startTime: dayAvailability.startTime,
      endTime: dayAvailability.endTime,
    },
  };

  res.status(200).json({
    success: true,
    availableTimes: availableTimesForSelectedDate,
  });
});

